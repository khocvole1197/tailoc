 <p>Theo quy hoạch kho số, toàn bộ các đầu số 01 (các thuê bao 11 số) sẽ được thu hồi, để phục vụ kết nối với những thiết bị Internet vạn vật (IoT) đáp ứng yêu cầu phát triển công nghiệp 4.0 của nền kinh tế số, giúp phát triển các loại hạ tầng thông minh như giáo dục, y tế, giao thông hay các thiết bị gia dụng, phục vụ và tránh tình trạng thiếu kho số di động và thừa kho số cố định.</p>

<p>Bộ Thông tin Truyền thông khẳng định việc quy hoạch kho số viễn thông quốc gia là cần thiết và phù hợp với thông lệ quốc tế. Không chỉ tại Việt Nam mà khá nhiều nước trên thế giới cũng thường điều chỉnh kho sim số sau khoảng 10-15 năm để đáp ứng nhu cầu phát triển xã hội. </p>

<p>Việc chuyển đổi sim từ 11 số về 10 số lần này không ảnh hưởng gì đến các thuê bao sim 10 số hiện nay mà chỉ tác động đến khoảng 60 triệu thuê bao 11 số. Đầu số rút gọn SIM 11 số: Viettel nhận đầu số 03, MobiFone nhận đầu số 07, VinaPhone nhận đầu số 08 </p>

<p>Các thuê bao 11 số khi chuyển sang thuê bao 10 số sẽ được giữ nguyên 7 số cuối. Ví dụ muốn gọi tới số sim 11 số cũ của VinaPhone là (0123).4567890, thì sau khi chuyển sang 10 số người ta sẽ gọi đến số (083).4567890. Nghĩa là chỉ thay mã mạng cũ (0123) sang mã mạng mới là (083). </p>
