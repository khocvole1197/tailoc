<p>
Thông thường người bán sim uy tín sẽ sở hữu một sim số đẹp. Đây có thể là “thước đo” để bạn hàng đánh giá uy tín của người bán.</p>
<p>
Trước khi mua bạn phải chắc chắn sim là của mình</p>
<p>
Rất nhiều người mua đã bị lừa mua phải sim dùng rồi hay sim đã kích hoạt. Không nên nhầm lẫn giữa sim mới 100% và sim nguyên kit.</p>
<p>
Đơn vị cũng cấp sim có thiết bị riêng gọi là hộp kích hoạt. Bạn chỉ cần nhét cả thẻ sim lớn chưa bẻ sim vào rồi nối với máy di động để kích. </p>
<p>
Trong trường hợp nếu người bán chủ tâm lừa đảo, họ sẽ kích hoạt sim bằng những thông tin cá nhân của họ và gọi đi/đến 5 - 10 cuộc điện thoại. Sau khi bạn đã nhận được sim còn nguyên kit và người bán đã nhận được tiền, thì họ sẽ tới trung tâm di động và khai báo 5-10 cuộc gọi đi gọi đến và lấy lại sim.</p>
<p>
Phải đảm bảo thông tin về sim là bảo mật khi đã sở hữu sim</p>
<p>
Nếu bạn để lộ thông tin cuộc gọi đến và gọi đi hay vì một lý do nào đó không dùng sim trong một khoảng thời gian dài (khoảng vài tháng). Thì người biết thông tin của sim có thể tới trung tâm di động và xin cấp lại số sim đó sau khi khai 5-10 cuộc gọi đến/gọi đi của sim.</p>
<p>
Với những sim số đắt tiền hãy đăng kí trả sau</p>
<p>
